#include <stdio.h>
int main()
{
    int num1, num2;

    printf("Input First number: ");
    scanf("%d", &num1);
    printf("Input Second number: ");
    scanf("%d", &num2);

    printf("The sum is %d\n", num1 + num2);
    printf("The difference is %d\n", num1 - num2);
    printf("The product is %d\n", num1 * num2);
    printf("The quotient is %d\n", num1 / num2);

    return 0;


}

